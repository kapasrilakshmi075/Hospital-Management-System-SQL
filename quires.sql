-----Top 5 busiest doctors-----
SELECT
d.doctor_id,
d.first_name,
d.last_name,
COUNT(a.appointment_id) AS total_appointments
FROM Doctors d
JOIN Appointments a
ON d.doctor_id = a.doctor_id
GROUP BY d.doctor_id, d.first_name, d.last_name
ORDER BY total_appointments DESC
LIMIT 5;

-----Top 5 patients by total spending-----
SELECT
p.patient_id,
p.first_name,
p.last_name,
SUM(b.total_amount) AS total_spent
FROM Patients p
JOIN Bills b
ON p.patient_id = b.patient_id
GROUP BY p.patient_id, p.first_name, p.last_name
ORDER BY total_spent DESC
LIMIT 5;

-----Revenue generated by each doctor-----
SELECT
d.doctor_id,
d.first_name,
d.last_name,
SUM(b.total_amount) AS revenue
FROM Doctors d
JOIN Appointments a
ON d.doctor_id = a.doctor_id
JOIN Bills b
ON a.patient_id = b.patient_id
GROUP BY d.doctor_id, d.first_name, d.last_name
ORDER BY revenue DESC;

-----Rank bills by amount-----
SELECT
bill_id,
total_amount,
RANK() OVER (ORDER BY total_amount DESC) AS bill_rank
FROM Bills;

-----Running total revenue-----
SELECT
bill_date,
total_amount,
SUM(total_amount) OVER (ORDER BY bill_date) AS running_total
FROM Bills;

-----Most prescribed medicine-----
SELECT
medicine_id,
COUNT(*) AS times_prescribed
FROM Prescriptions
GROUP BY medicine_id
ORDER BY times_prescribed DESC
LIMIT 1;

