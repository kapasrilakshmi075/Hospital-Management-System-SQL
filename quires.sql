-----Patient details with appointment dates-----
SELECT p.first_name, p.last_name, a.appointment_date
FROM Patients p
JOIN Appointments a
ON p.patient_id = a.patient_id;

-----Doctor names with departments-----
SELECT d.first_name, d.last_name, dep.department_name
FROM Doctors d
JOIN Departments dep
ON d.department_id = dep.department_id;

-----Appointment details with patient and doctor-----
SELECT
p.first_name,
p.last_name,
d.first_name AS doctor_first_name,
d.last_name AS doctor_last_name,
a.appointment_date
FROM Appointments a
JOIN Patients p
ON a.patient_id = p.patient_id
JOIN Doctors d
ON a.doctor_id = d.doctor_id;

-----Bills with patient names-----
SELECT
p.first_name,
p.last_name,
b.total_amount,
b.payment_status
FROM Bills b
JOIN Patients p
ON b.patient_id = p.patient_id;

-----Treatment details with patient names-----
SELECT
p.first_name,
p.last_name,
t.diagnosis,
t.treatment_cost
FROM Treatments t
JOIN Appointments a
ON t.appointment_id = a.appointment_id
JOIN Patients p
ON a.patient_id = p.patient_id;

-----Revenue by payment status-----
SELECT payment_status,
SUM(total_amount) AS revenue
FROM Bills
GROUP BY payment_status;

-----Monthly revenue-----
SELECT
EXTRACT(MONTH FROM bill_date) AS month,
SUM(total_amount) AS revenue
FROM Bills
GROUP BY month
ORDER BY month;

-----Doctors by specialization-----
SELECT specialization,
COUNT(*) AS total_doctors
FROM Doctors
GROUP BY specialization;

-----Medicines above average price-----
SELECT *
FROM Medicines
WHERE price > (
SELECT AVG(price)
FROM Medicines
);

